#7) Fazer um algoritmo que pergunte 3 números e apresente a média aritmética entre estes 3 números

print("Digite três números e lhe direi a média aritmética deles.")
v1 = float(input("Informe o primeiro número: "))
v2 = float(input("Informe o segundo número: "))
v3 = float(input("Informe o terceiro número: "))

media = (v1 + v2 + v3) / 3

print("Sua média das notas é ", media)